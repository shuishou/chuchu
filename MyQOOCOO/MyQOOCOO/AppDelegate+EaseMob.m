-(void)didReceiveMessage:(EMMessage *)message
{
    BOOL needShowNotification = (message.messageType != eMessageTypeChat) ? [self needShowNotification:message.conversationChatter] : YES;
    if (needShowNotification) {
#if !TARGET_IPHONE_SIMULATOR
        
        //        BOOL isAppActivity = [[UIApplication sharedApplication] applicationState] == UIApplicationStateActive;
        //        if (!isAppActivity) {
        //            [self showNotificationWithMessage:message];
        //        }else {
        //            [self playSoundAndVibration];
        //        }
        UIApplicationState state = [[UIApplication sharedApplication] applicationState];
        switch (state) {
            case UIApplicationStateActive:
                [self playSoundAndVibration];
                break;
            case UIApplicationStateInactive:
                [self playSoundAndVibration];
                break;
            case UIApplicationStateBackground:
            {
                NSUserDefaults * QCNotUserDe = [NSUserDefaults standardUserDefaults];
                NSString * notValues = [QCNotUserDe objectForKey:@"设置消息通知"];
                NSString * notsValues = [QCNotUserDe objectForKey:@"设置通知显示"];
                
                if (notValues.length == 0 && notsValues.length == 0) {
                    [self showNotificationWithMessage:message];
                }
                
            }
                break;
            default:
                break;
        }
#endif
    }
}

